/*
    Métodos de Aprendizado de Máquina
        O aprendizado de máquina se divide em várias abordagens, cada uma com suas características e aplicações específicas:

    Aprendizado Supervisionado: 
        Aqui, as máquinas aprendem com dados rotulados. Por exemplo, determinar se um e-mail é spam com base em exemplos marcados.
    Aprendizado Não Supervisionado: 
        Neste método, as máquinas analisam e encontram padrões em dados não rotulados, como agrupar clientes por preferências de compra.
    Aprendizado por Reforço: 
        As máquinas aprendem por tentativa e erro, aprimorando-se através de recompensas, como um programa de IA que aprende a jogar um jogo.
*/